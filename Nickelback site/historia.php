                <h2>O Começo - 1995</h2>
                
                <p>O Nickelback é uma banda de rock canadense, que possui vários sucessos e participações em trilhas sonoras.
                Eles surgiram na cidade de Hanna no ano de 1995, quando o vocalista Chad Kroeger, convidou o irmão Mike Kroeger e o amigo Ryan Peake para formar uma banda. 
                O cargo de baterista já foi ocupado por diversos músicos começando com Brandon Kroeger, primo de Chad e Mike.</p>
                <hr>
                <h2>Profissionalização</h2>
                <p>O primeiro álbum foi gravado de forma independente em 1996. Porém foi só em 1999, 
                que eles conseguiram gravar um disco mais profissional e conquistar seu próprio público. 
                O álbum recebeu o nome de The State, sendo lançado nos Estados Unidos em março de 2000. 
                Os singles "Breathe", "Leader of Men" e "Old Enough", se tornaram hits nas rádios americanas.</p>
                <hr>
                <h2>How You Remind Me</h2>
                <p>A banda já era conhecida no mercado norte-americano, mas foi em 2001 com o single "How You Remind", 
                que eles se consagraram no mercado mundial. Dizem que, esse sucesso do Nickelback, foi escrito em apenas quinze minutos, 
                em um intervalo entre um ensaio e outro. O mais incrível é que esse improviso rendeu ao Nickelback quatro Grammys e quatro prêmios na Billboard.</p>
                <hr>
                <h2>Silver Side Up</h2>
                <p>Nesse mesmo ano foi lançado o álbum "Silver Side Up", com bastante rock e uma pitada de heavy metal. 
                Além do sucesso de "How You Remind", nesse álbum eles também fizeram sucesso com as músicas "Too Bad" e "Never Again". 
                O Nickelbak chegou a receber o disco de platina em vários países da Europa, como Alemanha, Holanda e Austrália.</p> 
                <hr>
                <h2>The Long Road</h2>
                <p>Como a banda estava fazendo sucesso nos quatro cantos do globo, a gravadora não queria perder tempo. 
                Em 2003 eles já estavam lançando o "The Long Road", que ficou caracterizado como um trabalho com som agressivo e temática emocional. 
                O álbum reafirmou o sucesso da banda, vendendo cerca de nove milhões de cópias.</p>
                <hr>
                <h2>All Right Reasons e Dark Horse</h2>
                <p>Em 2005, "All Right Reasons" era o nome do mais novo álbum da banda. Foram sete singles lançados e todos eles foram um grande sucesso. 
                Após esse álbum veio o "Dark Horse", considerado um trabalho maduro, que embora não tenha feito sucesso no Brasil, chegou a ser platina tripla no mercado norte-americano.
                Atualmente os integrantes do Nickelback moram em Vancouver no Canadá e estão descansando entre um sucesso e outro.</p>
                <hr>
                <h2>Curiosidades</h2>
                <p>A música "Side of a Bullet" foi composta em homenagem ao falecido guitarrista do Pantera, Dimmebag Darrell Abbott, 
                    e conta com um dos últimos solos criados pelo mesmo, doado ao Nickelback por seu irmão e companheiro de banda, Vinnie Paul.</p>

                <p>O clipe da música "Rockstar" tem participações de muitos artistas entre eles Victoria Beckham, Gene Simmons (Baixista e vocal do Kiss), 
                    Ellen Jabour, Nelly Furtado, estrelas pornôs, anônimos entre outros.</p>
                
                <p>No dia 19 de Agosto de 2002, no Festival Ilha do Ermal em Portugal, os Nickelback abandonaram o palco durante a primeira música, 
                    depois de voarem pedras e garrafas de água para o palco em forma de protesto dos portugueses contra a banda por esta ser considerada 
                    demasiado pop/comercial para um festival de metal.</p>
                
                <p>No início de 2005, Ryan Vikedal, baterista da banda, abandonou o grupo por alegados desentendimentos acerca do novo álbum, 
                    foi substituído por Daniel Adair, que fazia parte dos 3 Doors Down.</p>
                
                <p>"Far Away" aparece no final de um episódio do seriado Cold Case da quinta temporada, episódio 18 - "Ghost of My Child".</p>
                
                <p>Chad Kroeger foi preso aos 14 anos por vandalismo e já teve outras passagens pela polícia por assaltos a própria escola.</p>
                
